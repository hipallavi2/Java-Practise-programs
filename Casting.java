
public class Casting {

	public static void main(String[] args) {
		int i = 97;
		System.out.println(i);
		int i2 = 'a';
		System.out.println(i2);
		char ch = (char)i;
		System.out.println(ch);
		int i3 = -97;
		System.out.println(i3);
		char ch1 = (char)i3;
		System.out.println(ch1);
		float f ='a';
		System.out.println(f);
	}

}
